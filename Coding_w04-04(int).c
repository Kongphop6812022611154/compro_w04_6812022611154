#include <stdio.h>

int main() {
    int score = 90.5;                    // int ใช้สำหรับเก็บค่าจำนวนเต็ม
    printf("a: %f\n", score);
}

//1.int จะแสดง score = 90 เพราะจะแสดงแค่ค่าจำนวนเต็ม
//2.ต้องเลือกใช้ Data Type ให้ถูกต้องเพื่อได้ค่าที่ถูกต้องที่สุด
//3.สรุปคือ int เหมาะสำหรับใช้กับค่าที่เป็นจำนวนเต็มไม่ควรใช้กับค่าที่เป็นจุดทศนิยม
