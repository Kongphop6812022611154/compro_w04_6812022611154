#include <stdio.h>                      // เติม # หน้า include และเติมเครื่องหมาย <>หัวและท้ายของ stdio.h

int main() {               
    int a = 10;                         // กำหนดตัวแปร a = 10 และเติม ; ท้ายประโยค
    printf("a = %d", a);                // แสดงตัวแปร a และเติม ; ท้ายประโยค
  
    return 0;                           // เสร็จสิ้นการทำงาน และเติม ; ต่อท้ายเลข 0
}
